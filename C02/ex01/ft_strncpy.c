#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while(src[i] != '\0'  && i <= n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int main()
{
	char	str[] = "najdaoui";
	char	des[4];

	ft_strncpy( des, str, 4);

	printf("%s\n", des);
}
